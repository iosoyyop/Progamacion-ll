class Stadistics{

    private int life;
    private int counter;

    public Stadistics(){
        life=3;
        counter=0;
    }

    public int getCounter() {
        return counter;
    }
    public int getLife() {
        return life;
    }
    public void setCounter(int counter) {
    this.counter = counter;
    }
    public void setLife(int life) {
    this.life = life;
    }
}